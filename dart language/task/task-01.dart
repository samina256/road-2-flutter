void main() {
  const int a = 7, b = 3;

  int add = a + b;
  int sub = a - b;
  int multi = a * b;
  double division = a / b;
  int mod = a % b;

  print('a = $a , b = $b');
  print('Addition: $a + $b = $add ');
  print('Subtruction: $a - $b = $sub ');
  print('Multiplication: $a * $b = $multi');
  print('Division : $a / $b = $division ');
  print('Modulus : $a % $b = $mod');
}
